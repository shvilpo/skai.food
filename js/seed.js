export async function ensureSeed() {
  const done = await db.get('settings', 'seeded');
  if (done) return;
  const existing = await db.count('products');
  if (existing === 0) {
    for (const [name, kcal, protein, fiber, plantPercent] of SEED) {
      await db.put('products', {
        id: uid(),
        name,
        per100: { kcal, protein, fiber },
        plantPercent,
        source: 'seed',
        usedCount: 0,
        createdAt: Date.now(),
      });
    }
  }
  await db.put('settings', { key: 'seeded', value: true });
}
